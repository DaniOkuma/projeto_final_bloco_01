package projeto_final_bloco_1.model;

public class Facial extends Cosmeticos {
	
	private String pele;

	public Facial(int numero, int tipo, String nome, float preco, String pele) {
		super(numero, tipo, nome, preco);
		this.pele = pele;

	}

	public String getPele() {
		return pele;
	}

	public void setPele(String pele) {
		this.pele = pele;
	}
	
    @Override
	public void visualizar() {
		super.visualizar();
		System.out.println("Tipo de pele: " + this.pele);
}
}